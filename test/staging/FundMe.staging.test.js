const { getNamedAccounts, ethers, network } = require("hardhat")
const { developmentChains } = require("../../helper-hardhat-config")
const { assert } = require("chai")
const { inputToConfig } = require("@ethereum-waffle/compiler")

developmentChains.includes(network.name)
    ? describe.skip
    : describe("FundMe", async function () {
          let fundMe
          let deployer
          const sendValue = ethers.utils.parseEther("0.04")

          beforeEach(async function () {
              deployer = (await getNamedAccounts()).deployer
              fundMe = await ethers.getContract("FundMe", deployer)
          })

          it("allows people to fund and withdraw", async function () {
              // console.log("funding...")
              //   let transactionResponse = await fundMe.fund({ value: sendValue })
              //   let transactionReceipt = await transactionResponse.wait(1)
              //   console.log("withdrawing...")
              //   transactionResponse = await fundMe.withdraw()
              //   transactionReceipt = transactionResponse.wait(1)
              await fundMe.fund({ value: sendValue })
              await fundMe.withdraw()
              const endingFundMeBalance = await fundMe.provider.getBalance(
                  fundMe.address
              )
              assert.equal(endingFundMeBalance.toString(), "0")
          })
      })
